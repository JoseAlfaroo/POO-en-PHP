<?php

$x = 4.5;
$y = 5;

echo $x, "<br>";

# Suma en asignacion
$x += $y;
echo $x, "<br>";

# Resta en asignacion
$x -= $y;
echo $x, "<br>";

# Multiplicacion en asignacion
$x *= $y;
echo $x, "<br>";

# Division en asignacion
$x /= $y;
echo $x, "<br>";

# Módulo en asignación
$x %= $y;
echo $x, "<br>";

?>